namespace Fractals1._7
{
    /// <summary>
    /// The main class for fractals.
    /// </summary>
    class Fractal
    {
        public int Recursion_depth;
        /// <summary>
        /// Constructor for setting the recursion depth.
        /// </summary>
        /// <param name="recursion_depth">Recursion depth.</param>
        public Fractal(int recursion_depth)
        {
            Recursion_depth = recursion_depth;
        }
    }
}
